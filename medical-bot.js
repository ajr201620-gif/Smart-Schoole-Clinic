
// روبوت تشخيص طبي مبسط
function medicalBot(symptoms) {
  if (symptoms.includes("حمى")) {
    return "يرجى قياس درجة الحرارة ومراجعة الطبيب إذا استمرت الأعراض.";
  }
  if (symptoms.includes("سعال")) {
    return "ينصح بالراحة وشرب السوائل. إذا كان السعال شديداً، راجع الطبيب.";
  }
  return "يرجى مراجعة العيادة المدرسية لمزيد من التقييم.";
}
